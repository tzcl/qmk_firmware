void render_layers(int x, int y) {
    oled_set_cursor(x, y);
    oled_write("LAYER", false);

    oled_set_cursor(x, y + 2);
    oled_write("BASE", layer_state <= 1);
    oled_set_cursor(x, y + 3);
    oled_write("SYMS", layer_state == 2);
    oled_set_cursor(x, y + 4);
    oled_write("NUMS", layer_state == 4);
    oled_set_cursor(x, y + 5);
    oled_write("NAV", layer_state == 8);
}

void render_wpm(int x, int y) {
    uint8_t n = get_current_wpm();
    char    wpm_str[4];
    oled_set_cursor(x, y);
    wpm_str[3] = '\0';
    wpm_str[2] = '0' + n % 10;
    wpm_str[1] = '0' + (n /= 10) % 10;
    wpm_str[0] = '0' + n / 10;
    oled_write(wpm_str, false);

    oled_set_cursor(x, y + 1);
    oled_write(" wpm", false);
}
