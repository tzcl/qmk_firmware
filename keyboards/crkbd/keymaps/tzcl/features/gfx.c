void luna_bark(bool _barking) {
    isBarking = _barking;
}

void luna_sneak(bool _sneaking) {
    isSneaking = _sneaking;
}

void luna_jump() {
    showedJump = false;
}

/* animation */
static void animate_luna(int x, int y) {
    /* jump */
    if (!showedJump) {
        /* clear */
        oled_set_cursor(x, y + 2);
        oled_write("     ", false);

        oled_set_cursor(x, y - 1);

        showedJump = true;
    } else {
        /* clear */
        oled_set_cursor(x, y - 1);
        oled_write("     ", false);

        oled_set_cursor(x, y);
    }

    /* switch frame */
    current_frame = (current_frame + 1) % 2;

    /* current status */
    if (isBarking) {
        oled_write_raw_P(bark[abs(1 - current_frame)], ANIM_SIZE);
    } else if (isSneaking) {
        oled_write_raw_P(sneak[abs(1 - current_frame)], ANIM_SIZE);
    } else if (current_wpm <= MIN_WALK_SPEED) {
        oled_write_raw_P(sit[abs(1 - current_frame)], ANIM_SIZE);

    } else if (current_wpm <= MIN_RUN_SPEED) {
        oled_write_raw_P(walk[abs(1 - current_frame)], ANIM_SIZE);

    } else {
        oled_write_raw_P(run[abs(1 - current_frame)], ANIM_SIZE);
    }
}

void render_luna(int x, int y) {
    current_wpm = get_current_wpm();

    if (timer_elapsed32(anim_sleep) < OLED_TIMEOUT || current_wpm > 0) {
        if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION) { /* run animation */
            anim_timer = timer_read32();
            animate_luna(x, y);
        }

        if (current_wpm > 0) {
            anim_sleep = timer_read32(); /* reset sleep timer */
        }
    } else {
        /* We have to manually turn the oleds off since changing frame resets the internal timer */
        oled_off();
    }
}

